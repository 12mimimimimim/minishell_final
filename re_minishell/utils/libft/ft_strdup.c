#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dst;
	size_t	i;

	dst = (char *) malloc(ft_strlen((char *)src) + 1);
	if (!dst)
	{
		return (NULL);
	}
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
