#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substr;
	size_t	i;
	size_t	s_tlen;

	if (!s)
		return (NULL);
	s_tlen = ft_strlen(s);
	if (start >= s_tlen)
		return (ft_strdup(""));
	if (len > s_tlen - start)
		len = s_tlen - start;
	substr = (char *)malloc(sizeof(char) * (len + 1));
	if (!substr)
		return (NULL);
	i = 0;
	while (i < len)
	{
		substr[i] = s[start + i];
		i++;
	}
	substr[i] = '\0';
	return (substr);
}
